#!/usr/bin/env node
/**
 * Generate DID + secret key pairs for APP_SK fields used by Payment Kit
 * and AIGNE Hub CF workers.
 *
 * Both workers use the same wallet type: Arcblock application role,
 * ED25519 key, SHA3 hash (matches `payment-kit/blocklets/core/cloudflare/shims/blocklet-sdk/wallet.ts`).
 *
 * Usage:
 *
 *   # Run from within a repo that has @ocap/wallet in its node_modules
 *   # (aigne-hub or payment-kit — either works)
 *   node docs/migration/scripts/generate-credentials.mjs
 *
 *   # Generate multiple credentials at once (for multi-worker deploy)
 *   node docs/migration/scripts/generate-credentials.mjs --labels=hub,payment
 *
 * Output format (stdout, one credential per --label):
 *
 *   === hub ===
 *   DID:        z1AbCDefGhIJklMnOpQrStUvWxYZ012345678
 *   APP_SK:     <64-char hex>
 *   publicKey:  <66-char hex>
 *
 * SECURITY:
 *   - Never paste the output into chat, issues, or commit logs.
 *   - Store APP_SK immediately via `wrangler secret put APP_SK -c <wrangler-config>`.
 *   - The DID printed here is safe to share — it is the public identity.
 *   - APP_PID is NOT generated by this script. APP_PID is the instance DID
 *     registered inside blocklet-service; see Phase 0.4 of EXECUTION_PLAYBOOK.md
 *     for how to obtain it.
 */

import * as Mcrypto from '@ocap/mcrypto';
import { fromRandom, WalletType } from '@ocap/wallet';

const args = process.argv.slice(2);
let labels = ['default'];
for (const arg of args) {
  if (arg.startsWith('--labels=')) {
    labels = arg.split('=')[1].split(',').map((s) => s.trim()).filter(Boolean);
  }
  if (arg === '--help' || arg === '-h') {
    console.log(`Usage: node generate-credentials.mjs [--labels=label1,label2]`);
    process.exit(0);
  }
}

const walletType = WalletType({
  role: Mcrypto.types.RoleType.ROLE_APPLICATION,
  pk: Mcrypto.types.KeyType.ED25519,
  hash: Mcrypto.types.HashType.SHA3,
});

for (const label of labels) {
  const wallet = fromRandom(walletType);
  console.log(`=== ${label} ===`);
  console.log(`DID:        ${wallet.address}`);
  console.log(`APP_SK:     ${wallet.secretKey}`);
  console.log(`publicKey:  ${wallet.publicKey}`);
  console.log('');
  console.log(`# Set as a CF worker secret (adjust config path):`);
  console.log(`printf '%s' "${wallet.secretKey}" | wrangler secret put APP_SK -c cloudflare/wrangler.staging.toml`);
  console.log('');
}
